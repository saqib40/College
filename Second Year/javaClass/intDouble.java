public class intDouble {
    public static void main(String[] args) {
        // integer to double
        int myNum = 50;
        System.out.println("The integer value is: " + myNum);
        double correspondingDouble = myNum;
        System.out.println("The corresponding double value is: " + correspondingDouble);

        // double to integer
        double myDouble = 50.55;
        System.out.println("The double value is: " + myDouble);
        int correspondingInt = (int) myDouble;
        System.out.println("The corresponding integer value: " + correspondingInt);
    }
}
